def get_playlist_tracks_with_previews(user, playlist_id):
    raw_tracks = get_playlist_tracks(user, playlist_id)
    tracks_with_preview = []

    for item in raw_tracks:
        track = item["track"]
        track_id = track["id"]
        track_info = get_track_info(user, track_id)
        tracks_with_preview.append(
            {
                "name": track_info["name"],
                "image_url": (
                    track_info["album"]["images"][0]["url"]
                    if track_info["album"]["images"]
                    else None
                ),
                "preview_url": track_info.get("preview_url"),
                "spotify_url": track_info["external_urls"]["spotify"],
                "artists": [artist["name"] for artist in track_info["artists"]],
            }
        )

    return tracks_with_preview


